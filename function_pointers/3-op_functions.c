/**
 * op_add - Addition
 * @a: num1
 * @b: num2
 */
int op_add(int a, int b)
{
	return a + b;
}

/**
 * op_sub - Subtraction
 * @a: num1
 * @b: num2
 */
int op_sub(int a, int b)
{
	return a - b;
}

/**
 * op_mul - Multiplication
 * @a: num1
 * @b: num2
 */
int op_mul(int a, int b)
{
	return a * b;
}

/**
 * op_div - Division
 * @a: num1
 * @b: num2
 */
int op_div(int a, int b)
{
	return a / b;
}

/**
 * op_mod - Modulus
 * @a: num1
 * @b: num2
 */
int op_mod(int a, int b)
{
	return a % b;
}
